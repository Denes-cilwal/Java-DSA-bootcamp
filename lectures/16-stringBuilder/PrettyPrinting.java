public class PrettyPrinting {
    public static void main(String[] args) {
        float a = 453.1234f;
        System.out.printf("formatted number is %.2f", a); // formatted to 2 after decimal and rounded of
        System.out.println();
        System.out.printf("Pie : %3f", Math.PI);
        System.out.println();
        System.out.printf("Hello my name is %s and I am %s", "Dinesh", "cool");
    }
}
