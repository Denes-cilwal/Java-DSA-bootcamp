package com.dinesh;

public class Switch {

     // different button , different value
     public static void main(String[] args) {
         int num = 2;
         // switch(variable) > compare with case num
         switch (num){
                 case 1:
                 System.out.println("burger");
                 break;
                 case 2:
                 System.out.println("mango shake");
                 break;
                 default:
                 System.out.println("wake up get coffee");
         }
     }
}
